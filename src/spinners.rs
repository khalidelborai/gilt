use std::sync::LazyLock;
use std::collections::HashMap;

/// Data for a single spinner animation: the interval between frames and the frames themselves.
pub struct SpinnerData {
    /// Milliseconds between frames.
    pub interval: f64,
    /// Animation frames (each element is one frame to display).
    pub frames: Vec<String>,
}

/// Split a string into individual characters, each becoming a frame.
fn chars_to_frames(s: &str) -> Vec<String> {
    s.chars().map(|c| c.to_string()).collect()
}

/// Convert a slice of `&str` into owned `Vec<String>`.
fn strs_to_frames(v: &[&str]) -> Vec<String> {
    v.iter().map(|s| s.to_string()).collect()
}

